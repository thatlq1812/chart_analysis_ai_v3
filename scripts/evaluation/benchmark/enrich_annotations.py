"""
Enrich benchmark annotations from chart_qa_v2 QA pairs.

The QA pairs (generated by Gemini Vision) contain ground truth information
about chart titles, data values, element counts, axis ranges, etc.
This script extracts that information and populates the benchmark
annotation files so the evaluator can properly score Stage 3 output.

Usage:
    .venv/Scripts/python.exe scripts/evaluation/benchmark/enrich_annotations.py

    Options:
        --dry-run   Show what would be changed without writing files
"""

import argparse
import json
import logging
import re
import sys
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s | %(levelname)s | %(message)s",
)
logger = logging.getLogger(__name__)

PROJECT_ROOT = Path(__file__).resolve().parents[3]
BENCHMARK_DIR = PROJECT_ROOT / "data" / "benchmark"
QA_DIR = PROJECT_ROOT / "data" / "academic_dataset" / "chart_qa_v2" / "generated"

# Map chart types to their primary element types for Stage 3
CHART_TYPE_TO_ELEMENT = {
    "bar": "bar",
    "histogram": "bar",
    "line": "point",
    "scatter": "point",
    "area": "point",
    "pie": "slice",
    "box": "box",
    "heatmap": "cell",
}


def find_qa_file(chart_id: str) -> Optional[Path]:
    """Find the QA file for a given chart ID across all type folders."""
    matches = list(QA_DIR.glob(f"*/{chart_id}.json"))
    if matches:
        return matches[0]
    return None


def extract_title(qa_pairs: List[Dict[str, Any]]) -> Optional[str]:
    """Extract chart title from QA pairs."""
    for q in qa_pairs:
        question_lower = q["question"].lower()
        if "title" in question_lower and q.get("answer"):
            answer = q["answer"].strip()
            # Clean common prefixes from Gemini answers
            for prefix in [
                "The title of the chart is ",
                "The title is ",
                "The chart title is ",
                "The chart is titled ",
            ]:
                if answer.lower().startswith(prefix.lower()):
                    answer = answer[len(prefix) :]
                    break
            # Remove trailing period and quotes
            answer = answer.strip(".").strip("'").strip('"').strip()
            if answer:
                return answer
    return None


def extract_element_count_from_qa(
    qa_pairs: List[Dict[str, Any]],
    chart_type: str,
) -> Optional[int]:
    """
    Infer element count from QA pairs.

    Strategies:
    - Direct "how many bars/slices/points" questions
    - Threshold questions mentioning count ("7 countries meet this threshold")
    - Count distinct named items in extraction/comparison answers
    """
    # Strategy 1: Direct count questions
    count_patterns = [
        r"how many (?:bars?|columns?|groups?|categories)",
        r"how many (?:slices?|segments?|sections?|portions?)",
        r"how many (?:data )?points?",
        r"how many (?:lines?|series|curves?)",
        r"number of (?:bars?|slices?|points?|lines?|categories)",
    ]
    for q in qa_pairs:
        question_lower = q["question"].lower()
        # Skip multi_hop / threshold / hypothetical questions
        # These compute derived values, not element counts
        if q.get("question_type") in ("multi_hop", "threshold", "interpolation"):
            continue
        if "if " in question_lower[:5]:  # Hypothetical "If the total is 1000..."
            continue
        for pattern in count_patterns:
            if re.search(pattern, question_lower):
                # Try answer_value first
                if q.get("answer_value") is not None:
                    val = q["answer_value"]
                    if isinstance(val, (int, float)) and 1 < val < 100:
                        return int(val)
                # Try parsing from answer text
                match = re.search(r"(\d+)", q["answer"])
                if match:
                    count = int(match.group(1))
                    if 1 < count < 100:
                        return count

    # Strategy 2: For bar charts, count distinct named items from extraction Qs
    if chart_type in ("bar", "histogram"):
        named_items = set()
        for q in qa_pairs:
            if q["question_type"] in ("extraction", "comparison"):
                # Look for "What is the value of X?" patterns
                m = re.search(
                    r"(?:value|speed|price|accuracy|score|percentage|count|number|rate|size)"
                    r".*?(?:of|for)\s+(.+?)(?:\?|$)",
                    q["question"],
                    re.IGNORECASE,
                )
                if m:
                    named_items.add(m.group(1).strip().rstrip("?"))
                # Also: "Which X has the highest/lowest Y?"
                m2 = re.search(
                    r"(?:which|what)\s+(\w+)\s+has\s+(?:the\s+)?(?:highest|lowest|largest|smallest)",
                    q["question"],
                    re.IGNORECASE,
                )
                if m2 and q.get("answer"):
                    # The answer names one item
                    named_items.add(q["answer"].strip().rstrip("."))
        if len(named_items) >= 2:
            return len(named_items)

    # Strategy 2b: For bar charts, also try counting from threshold answers
    if chart_type in ("bar", "histogram"):
        for q in qa_pairs:
            if q["question_type"] == "threshold" and q.get("answer_value") is not None:
                val = q["answer_value"]
                if isinstance(val, (int, float)) and 2 < val < 50:
                    # Threshold count is a LOWER bound of total bars
                    # "7 countries meet threshold" -> at least 7 bars
                    # We use it as estimate if nothing better found
                    return int(val)

    # Strategy 3: For pie charts, count segments from extraction answers
    if chart_type == "pie":
        segments = set()
        for q in qa_pairs:
            if q["question_type"] == "extraction" and q.get("answer_value") is not None:
                val = q["answer_value"]
                # Only count percentage-type values (0-100) as pie segments
                if isinstance(val, (int, float)) and 0 < val <= 100:
                    segments.add(q["question"])
        if segments:
            return max(len(segments), 2)  # At least 2 slices for a pie

    # Strategy 4: For line/scatter, count series from structural questions
    if chart_type in ("line", "scatter", "area"):
        series_mentions = set()
        for q in qa_pairs:
            if q["question_type"] == "structural":
                # "How many lines/series/curves are there?"
                m = re.search(r"how many (?:lines?|series|curves?)", q["question"].lower())
                if m and q.get("answer_value") is not None:
                    val = q["answer_value"]
                    if isinstance(val, (int, float)) and 1 < val < 50:
                        return int(val)

            # Count from "which line/series" comparison answers
            if q["question_type"] == "comparison":
                m = re.search(
                    r"(?:which|what)\s+(?:line|series|curve|color)",
                    q["question"].lower(),
                )
                if m and q.get("answer"):
                    series_mentions.add(q["answer"].strip()[:30].lower())

        # For line charts: number of series * data points per series is complex
        # Just use series count as a rough number
        if len(series_mentions) >= 2:
            return len(series_mentions)

    return None


def extract_axis_info(
    qa_pairs: List[Dict[str, Any]],
    chart_type: str,
) -> Dict[str, Optional[float]]:
    """
    Extract axis range information from QA pairs.

    Looks for questions about axis min/max, range, and x/y axis labels.
    """
    axis = {
        "x_min": None,
        "x_max": None,
        "y_min": None,
        "y_max": None,
        "x_label": None,
        "y_label": None,
    }

    for q in qa_pairs:
        question_lower = q["question"].lower()
        answer = q["answer"]

        # X-axis label
        if "x-axis" in question_lower and (
            "label" in question_lower
            or "plotted" in question_lower
            or "represent" in question_lower
            or "what is" in question_lower
        ):
            axis["x_label"] = answer.strip().rstrip(".")

        # Y-axis label
        if "y-axis" in question_lower and (
            "label" in question_lower
            or "plotted" in question_lower
            or "represent" in question_lower
            or "what is" in question_lower
        ):
            axis["y_label"] = answer.strip().rstrip(".")

        # Min/max extraction
        if q.get("answer_value") is not None:
            val = q["answer_value"]

            if "minimum" in question_lower or "lowest" in question_lower:
                if "x" in question_lower or "horizontal" in question_lower:
                    axis["x_min"] = val
                elif "y" in question_lower or "vertical" in question_lower:
                    axis["y_min"] = val
                else:
                    # Default: lowest value is likely y-axis for bar/line
                    if chart_type in ("bar", "line", "area", "histogram"):
                        axis["y_min"] = val

            if "maximum" in question_lower or "highest" in question_lower:
                if "x" in question_lower or "horizontal" in question_lower:
                    axis["x_max"] = val
                elif "y" in question_lower or "vertical" in question_lower:
                    axis["y_max"] = val
                else:
                    if chart_type in ("bar", "line", "area", "histogram"):
                        axis["y_max"] = val

            # Range questions
            if "range" in question_lower:
                # Try to parse "from X to Y"
                range_match = re.search(
                    r"(?:from|between)\s+([\d.]+)\s+(?:to|and)\s+([\d.]+)",
                    answer,
                )
                if range_match:
                    lo, hi = float(range_match.group(1)), float(range_match.group(2))
                    if "x" in question_lower:
                        axis["x_min"] = lo
                        axis["x_max"] = hi
                    elif "y" in question_lower:
                        axis["y_min"] = lo
                        axis["y_max"] = hi

    # Clean: if all axis values are None, return just labels
    has_any_value = any(
        axis[k] is not None for k in ("x_min", "x_max", "y_min", "y_max")
    )
    if not has_any_value and not axis["x_label"] and not axis["y_label"]:
        return {}

    return axis


def extract_texts(
    qa_pairs: List[Dict[str, Any]],
    title: Optional[str],
) -> List[Dict[str, str]]:
    """
    Extract text items from QA pairs for OCR evaluation.

    Includes title, axis labels, and named categories/labels.
    """
    texts = []
    seen = set()

    # Title
    if title:
        texts.append({"text": title, "role": "title"})
        seen.add(title.lower())

    # Axis labels from structural questions
    for q in qa_pairs:
        question_lower = q["question"].lower()
        if q["question_type"] == "structural":
            # Unit questions -> could be axis label
            if "unit" in question_lower:
                val = q["answer"].strip().rstrip(".")
                # Clean Gemini answers like "The unit is Mbps"
                for prefix in ["The unit is ", "The unit used is "]:
                    if val.startswith(prefix):
                        val = val[len(prefix) :]
                val = val.strip().rstrip(".")
                if val.lower() not in seen:
                    texts.append({"text": val, "role": "axis_label"})
                    seen.add(val.lower())

            # Axis label questions
            if ("x-axis" in question_lower or "y-axis" in question_lower) and (
                "label" in question_lower
                or "plotted" in question_lower
                or "represent" in question_lower
            ):
                val = q["answer"].strip().rstrip(".")
                # Clean prefixes
                for prefix in [
                    "The y-axis represents ",
                    "The x-axis represents ",
                    "The y-axis is ",
                    "The x-axis is ",
                ]:
                    if val.lower().startswith(prefix.lower()):
                        val = val[len(prefix) :]
                val = val.strip().rstrip(".")
                if val.lower() not in seen:
                    role = "y_axis_label" if "y-axis" in question_lower else "x_axis_label"
                    texts.append({"text": val, "role": role})
                    seen.add(val.lower())

    return texts


def extract_data_values(
    qa_pairs: List[Dict[str, Any]],
    chart_type: str,
) -> List[Dict[str, Any]]:
    """
    Extract individual data values from extraction-type QA pairs.

    These become the data_series for detailed value comparison.
    """
    values = []
    for q in qa_pairs:
        if q["question_type"] == "extraction" and q.get("answer_value") is not None:
            values.append(
                {
                    "question": q["question"],
                    "value": q["answer_value"],
                    "answer_text": q["answer"],
                    "confidence": q.get("inference", {}).get("confidence", 0.9),
                }
            )
    return values


def enrich_annotation(
    annotation: Dict[str, Any],
    qa_data: Dict[str, Any],
) -> Dict[str, Any]:
    """
    Enrich a single annotation with ground truth from QA data.

    Modifies annotation in-place and returns it.
    """
    chart_type = annotation["chart_type"]
    qa_pairs = qa_data.get("qa_pairs", [])

    if not qa_pairs:
        return annotation

    # 1. Title
    title = extract_title(qa_pairs)
    if title:
        annotation["title"] = title

    # 2. Element count
    element_count = extract_element_count_from_qa(qa_pairs, chart_type)
    if element_count is not None and element_count > 0:
        annotation["elements"]["element_count"] = element_count
        annotation["elements"]["primary_element_type"] = CHART_TYPE_TO_ELEMENT.get(
            chart_type, "unknown"
        )

    # 3. Axis info
    axis_info = extract_axis_info(qa_pairs, chart_type)
    if axis_info:
        if annotation.get("axis") is None:
            annotation["axis"] = {
                "x_axis_type": "categorical" if chart_type in ("bar", "histogram", "box") else "linear",
                "y_axis_type": "linear",
                "x_min": None,
                "x_max": None,
                "y_min": None,
                "y_max": None,
                "x_categories": None,
                "y_categories": None,
                "x_label": None,
                "y_label": None,
            }
        for key, val in axis_info.items():
            if val is not None:
                annotation["axis"][key] = val

    # 4. Texts
    texts = extract_texts(qa_pairs, title)
    if texts:
        annotation["texts"] = texts

    # 5. Data values (for reference, not directly used by evaluator yet)
    data_values = extract_data_values(qa_pairs, chart_type)
    if data_values:
        annotation["data_series"] = data_values

    # 6. Quality info from QA verification
    verification = qa_data.get("verification", {})
    if verification.get("chart_quality"):
        annotation["chart_quality"] = verification["chart_quality"]

    annotation["annotator"] = "gemini_qa_v2"

    return annotation


def main() -> None:
    """Enrich all benchmark annotations from chart_qa_v2 data."""
    parser = argparse.ArgumentParser(description="Enrich benchmark annotations from QA data")
    parser.add_argument("--dry-run", action="store_true", help="Show changes without writing")
    args = parser.parse_args()

    manifest_path = BENCHMARK_DIR / "benchmark_manifest.json"
    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    charts = manifest["charts"]

    logger.info(f"Processing {len(charts)} benchmark charts")

    stats = {
        "total": 0,
        "enriched": 0,
        "title_added": 0,
        "elements_added": 0,
        "axis_added": 0,
        "texts_added": 0,
        "qa_not_found": 0,
    }

    for chart in charts:
        chart_id = chart["chart_id"]
        chart_type = chart["chart_type"]
        stats["total"] += 1

        # Find QA file
        qa_file = find_qa_file(chart_id)
        if qa_file is None:
            logger.warning(f"No QA file found | chart_id={chart_id}")
            stats["qa_not_found"] += 1
            continue

        qa_data = json.loads(qa_file.read_text(encoding="utf-8"))

        # Load current annotation
        ann_path = BENCHMARK_DIR / "annotations" / f"{chart_id}.json"
        if ann_path.exists():
            annotation = json.loads(ann_path.read_text(encoding="utf-8"))
        else:
            logger.warning(f"No annotation file | chart_id={chart_id}")
            continue

        # Track what was empty before
        had_title = annotation.get("title") is not None
        had_elements = annotation.get("elements", {}).get("element_count", 0) > 0
        had_axis = (
            annotation.get("axis") is not None
            and any(
                annotation["axis"].get(k) is not None
                for k in ("x_min", "x_max", "y_min", "y_max")
            )
        )
        had_texts = len(annotation.get("texts", [])) > 0

        # Enrich
        enriched = enrich_annotation(annotation, qa_data)

        # Count changes
        changed = False
        if not had_title and enriched.get("title"):
            stats["title_added"] += 1
            changed = True
        if not had_elements and enriched.get("elements", {}).get("element_count", 0) > 0:
            stats["elements_added"] += 1
            changed = True
        if not had_axis and enriched.get("axis") and any(
            enriched["axis"].get(k) is not None
            for k in ("x_min", "x_max", "y_min", "y_max")
        ):
            stats["axis_added"] += 1
            changed = True
        if not had_texts and len(enriched.get("texts", [])) > 0:
            stats["texts_added"] += 1
            changed = True

        if changed:
            stats["enriched"] += 1

        # Log per chart
        elem_count = enriched.get("elements", {}).get("element_count", 0)
        title_str = (enriched.get("title") or "N/A")[:30]
        logger.info(
            f"  [{chart_type:9s}] {chart_id[:40]:40s} | "
            f"title={'Y' if enriched.get('title') else 'N'} | "
            f"elem={elem_count:3d} | "
            f"axis={'Y' if enriched.get('axis') and any(enriched['axis'].get(k) is not None for k in ('x_min','x_max','y_min','y_max')) else 'N'} | "
            f"texts={len(enriched.get('texts', []))}"
        )

        # Write
        if not args.dry_run:
            ann_path.write_text(
                json.dumps(enriched, indent=2, ensure_ascii=False),
                encoding="utf-8",
            )

    # Summary
    print("\n" + "=" * 60)
    print("ANNOTATION ENRICHMENT COMPLETE")
    print("=" * 60)
    print(f"Total charts:      {stats['total']}")
    print(f"Enriched:          {stats['enriched']}")
    print(f"Titles added:      {stats['title_added']}")
    print(f"Element counts:    {stats['elements_added']}")
    print(f"Axis info:         {stats['axis_added']}")
    print(f"Texts added:       {stats['texts_added']}")
    print(f"QA not found:      {stats['qa_not_found']}")
    if args.dry_run:
        print("\n[DRY RUN] No files were modified.")


if __name__ == "__main__":
    main()
